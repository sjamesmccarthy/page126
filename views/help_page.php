<a name="a28"></a>
<p class="question" style="padding-top: 10px;"><img onclick='toggle_answer('a28');'  src="images/more.png" /> <a href="#a27" onclick="toggle_answer('a28');">Why do I have to log back in after quitting my browser (session)?</a></p>
<p id="a28" class="answer">Each time that you quit your browser the session cookie is destroyed and will require you to log back in. We do this for security reasons so that if you quit your browser, leave your computer and someone else opens your browser, where you left off in pageonetwentysix.com will not display. Warning: Your session WILL NOT automatically time-out so when you are done, nor does closing the tab end your session. ALWAYS LOGOUT WHEN YOU ARE DONE.</p>

