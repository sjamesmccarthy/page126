<p id="a8" class="answer">The idea of PageOneTwentySix is to create a small tribe of fellow writers and help each other through the writing process, eventually creating a marketable and publishable work that can be shared with the world.<!-- Blogging is where people share their opinions with the public. At PageOneTwentySix, everything is private and for your eyes only. No one even knows you have an account but you. --></p>

