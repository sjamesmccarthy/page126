<p id="a21" class="answer">Yes, there are some basic shortcut key options. They are only available on the writing page and all use the ctrl key (ctrl-{letter}). Z - hides writing area, S - saves entry, N - creates new entry, J - displays listing of all journals, Q - logout, I - displays listing of all entries, D - move current entry to trash</p>

