<p id="a4" class="answer">Although a simple of level encrypting entries has been built in to the application. It currently is not enabled because the long-term vision of this PageOneTwentySix is to collaborate and share. Encryption on the entry and database level would slow the process of this goal. If you would like to see us enable this please <a target="_new" href="http://twitter.com/nomoreblacktea">drop us a tweet</a>.<!-- Yes! Privacy and security were our main design factors. Of course, if you leave your browser window for everyone to read, it's only as secure as make it, but on the programming side, we have made it extremely difficult for anyone but you to know what you have written. You can read more about how we <a href="?func=privacy">safeguard your privacy</a>. --></p>

