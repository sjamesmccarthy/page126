<a name="a6"></a>
<p class="question" style="padding-top: 10px;"><img onclick='toggle_answer('a6');'  src="images/more.png" /> <a href="#a5" onclick="toggle_answer('a6');">Can I export my entries and journal?</a></p>
<p id="a6" class="answer">Absolutely. You have the option to view and then individually email that entry to yourself, or go to your settings you can export a CSV archive of all your entries for specific journals or every journal. Data preservation and back-up is high on next feature list so keep an eye on our <a target="_new" href="http://twitter.com/nomoreblacktea">Twitter feed</a>.</p>

<a name="a7"></a>
<p class="question" style="padding-top: 10px;"><img onclick='toggle_answer('a7');'  src="images/more.png" /> <a href="#a6" onclick="toggle_answer('a7');">How does the trash work? Can I take items out of it?</a></p>
<p id="a7" class="answer">The Trash works like the Trash on Mac OS X or the Recycle Bin on Windows. When viewing an entry click the Trash icon or you can delete individual entries from pages view. This will move it to the Trash, but not delete the file. When you are ready to permanently delete the file you can go to your Settings and click "Manage Journals" and then from within the specific journal click the "Empty Trash" link.</p>

