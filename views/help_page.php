<p id="a29" class="answer">There really is nothing to it. Simply log in to your account and create or edit an entry. Autosave automatically works for you. It triggered when moving between form fields, such as typing or editing a title and then pressing tab or clicking into the text area field. It also watches for pauses while typing in the text area and after two seconds of inactivity your entry will be saved.</p>

