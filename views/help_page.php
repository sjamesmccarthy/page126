<a name="a10"></a>
<p class="question" style="padding-top: 10px;"><img onclick='toggle_answer('a10');'  src="images/more.png" /> <a href="#a9" onclick="toggle_answer('a10');">Can I change the date of entries after saving them?</a></p>
<p id="a10" class="answer">This option has currently been disabled (12/31/10). We believe the creation date should be authentic. We do track and update the modification date, much like a standard text file is created and modified by your computer's operating system.</p>

